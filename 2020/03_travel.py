def get_no_trees(down: int, right: int) -> int:
    length = len(world[0])
    index = 0
    number_trees = 0

    for i, row in enumerate(world):
        if i % down != 0:
            continue
        if row[index] == "#":
            number_trees += 1
        index += right
        index %= length

    return number_trees


def part_one():
    print(get_no_trees(1, 3))


def part_two():
    options = ((1, 1), (1, 3), (1, 5), (1, 7), (2, 1))
    result = 1
    for i in options:
        result *= get_no_trees(*i)
    print(result)


part_one()
part_two()
